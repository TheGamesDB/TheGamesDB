		<h2>General Guidelines</h2>
		<p>Please take a minute to read these guidelines. We are very excited about people getting together and helping to make something great, but we also need to work together to keep some sort of order around here!
		<br>
		<br>
		<u>TheGamesDb.net WIKI:</u><br>
		We ask that anyone who plans on becoming a part of TheGamesDB.net community please takes the time to read over the <a href=http://code.google.com/p/thegamesdb/w/list>WIKI</a> as it will make your experience more worthwhile. We know the WIKI isn&apos;t perfect. If you see something that needs to be added, edited or deleted, please post in the <a href=http://forums.thegamesdb.net/>forums</a>.
		<br>
		<br>
		<u>Search the Site:</u><br>
		Always do a search for a game before posting a new one.  Chances are, the game you want to post has already been submitted.  The titles may be different than what you were expecting, and if you have one that you feel is more correct for a currently posted game, please let us know in the <a href=http://forums.thegamesdb.net/>forums</a> and we will get it changed.  Never submit a game title that has any modifications (like the console name included).  A good practice is to see what the game is titled on Wikipedia.
		<br>
		<br>
		<u>Sources of information:</u><br>
		Your first stop for getting information should ALWAYS be an official site for the game. Sometimes, the official sites don&apos;t list complete information, so from there you'll want to hit sites that are either edited by professionals or checked for accuracy on a regular basis. These sites include wikipedia.org, gamespy.com, ign.com, and gamespot.com. For the titles of games, I generally follow the formatting used on Wikipedia since it&apos;s widely agreed upon. For standard game titles, MAKE SURE you capitalize the first letter in the title. If we have inconsistencies with capitalization, things will start to look very messy.
		<br>
		<br>
			<u>Being a Community:</u><br>
			We know that the site isn&apos;t perfect. That&apos;s why we need you! You think something&apos;s wrong? Question it! You think something is unclear? Let us know! You have the greatest idea for the site that it would eliminate all issues for the rest of time? PLEASE TELL US! We are listening! We understand that YOU are the key to success here, and suggestions and feature requests have an extremely good chance of being implemented. Ask away!
			<br>
			<br>
			<u>Uploading Images:</u><br>
			Artwork for games can be some amazing pieces of work. We love the way artwork makes the programs that use the API look. Without the artwork, we would just have text! With that being said, we ask that you be smart about the images that you upload. We encourage you to find artwork and create your own, but please don't upload anything that is blurry, pixelated, or any sort of low quality. One bad image is all it takes to ruin the look of a game. This is also why we ask that you follow the resolution requirements we have set for image types. Also, DO NOT upload images that contain nudity. This includes animated nudity. Just because it is a cartoon/anime does not make the nudity okay. Keep in mind that people of ALL AGES browse this site, and we need to protect children from viewing material deemed inappropriate by applicable laws. If you upload an image of poor quality or inappropriate material, your upload privileges will be disallowed.
			
			</p>
			
			<h2>All Image Guidelines</h2>
			<ul>
				<li>The Banner language should be set on upload to the language on the banner</li>
				<li>Images must be JPG or PNG.</li>
				<li>Each Creator is limited to 3 banners per game.  Please try and make each banner unique and not simply the same images with different fonts.</li>
				<li>While some games do contain nudity please refrain from using images which contain it.</li>
				<li>Do not take another artists banners to alter them slightly then re-upload them. Some artists have spent a good deal of time on the images they upload and it is not acceptable to alter their work without permission.</li>
			</ul>
			
				<h2>Fanart</h2>
			<ul>
				<li>Fanart must be 1280x720 or 1920x1080</li>
				<li>Images must be of good quality. We don&apos;t want blurry or pixelated images.</li>
				<li>Look at the image you're using... Can this be used for more than one game? If you have a picture that is just Mario, you can probably use this as artwork for multiple mario games. This is perfectly acceptable. However, if artwork is title specific, please don't use it on another title in the game series that it doesn't apply to. This would be something like a picture of a character that shows up in one game like Super Mario Bros. 2, but isn't in Super Mario Bros.</li>
				
			</ul>

			<h2>Banners</h2>
			<ul>
				<li>Banners must be 760 x 140</li>
				<li>Text banners generally use Arial Bold font, 27pt as the text.</li>
			</ul>

